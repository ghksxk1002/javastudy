package prob01;

public class Printer {
	
	public void println(int println) {
		System.out.println(println);
	}
	
	public void println(boolean println) {
		System.out.println(println);
	}
	
	public void println(double println) {
		System.out.println(println);
	}
	
	public void println(String println) {
		System.out.println(println);
	}
	
	
}
